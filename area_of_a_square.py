"""
program for calculatin the area of a cylinder
given keywords;
r stands for the radius of the cylinder,
h stands for the height of the cylinder
pi has the value of 3.142

"""
h = int(input("What is the value of the height of the cylinder? \n"))
r = int(input("What is the value of the right of the cylinder? \n"))
pi = 3.142
a = 2 * pi * r **2 + h ( 2 * pi * r )
print ("The area of the cylinder will be ",a)
